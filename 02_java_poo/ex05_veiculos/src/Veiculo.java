public class Veiculo {
    public String marca;
    public String modelo;
    public String anoDeFabricacao;
    public String cor;
    public String quilometragem;

    public Veiculo(String marca, String modelo, String anoDeFabricacao, String cor, String quilometragem) {
        this.marca = marca;
        this.modelo = modelo;
        this.anoDeFabricacao = anoDeFabricacao;
        this.cor = cor;
        this.quilometragem = quilometragem;
    }
}
